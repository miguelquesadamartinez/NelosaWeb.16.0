  Para evitar que los inyectores o cabezales de impresi&oacute;n se obstruyan o deterioren, rellene el cartucho antes de que se acabe la tinta, cuando los niveles de tinta marquen una tercera parte de capacidad o controlando peri&oacute;dicamente el peso del cartucho (10 ml. De tinta pesan 10 gramos).</p>
              <p><strong>Introduzca la tinta MUY LENTAMENTE</strong>. Las burbujas de aire no deber&iacute;an ser inyectadas en el cartucho, ya que producir&iacute;an fallos en la impresi&oacute;n o incluso deteriorar el cartucho. </p>
              <p>La cantidad de tinta a inyectar que orientamos es de: ( entre 4 y 7 ml cartucho peque&ntilde;o // entre 7 y 12 ml cartucho mediano // Hasta 20 ml. Grande).<br>
                Durante la recarga observe la esponja, si la tiene, cuando se oscurezca, ya tiene la tinta necesaria, si sigue inyectando la tinta le saldr&aacute; por los orificios de relleno. Un cartucho con exceso de tinta no le imprimir&aacute;, hay que sacar un poco de tinta y limpiar el cabezal.</p>
              <p>Despu&eacute;s de recargar el cartucho puede gotear hasta que expulse la tinta sobrante; En la mayor&iacute;a de cartuchos, es recomendable dejarlo reposar, en posici&oacute;n vertical, durante media hora para que la tinta llegue a los cabezales y expulse la tinta sobrante.<br>
                <strong>Forzar la extracci&oacute;n de tinta a trav&eacute;s del cabezal, en los cartuchos individuales de Epson</strong></p>
              <p><strong>Cartuchos HP tinta negra (Excepto 26, 56 10 y 116 )</strong>: aseg&uacute;rese de tapar bien el orificio de relleno, y presionar los laterales del cartucho mientras tapa el orificio de relleno, para crear el vac&iacute;o necesario para que la tinta no gotee<br>
                Para finalizar la recarga, una vez montado el cartucho, utilice el software de la impresora para limpiar los cabezales, para despu&eacute;s imprimir una hoja de prueba, si requiere de m&aacute;s de dos limpiezas, deber&aacute; dejar un intervalo de 5 min entre ellas.</p>
              <p>No deje el cartucho mas de 2 minutos sobre una superficie porosa, como un cart&oacute;n o gamuza, esto podr&iacute;a vaciar la tinta del cartucho, d&eacute;jelo reposar sobre una superficie de pl&aacute;stico.<br>
                Limpie la jeringuilla con agua cada vez que cambie de color y al finalizar la recarga.</p>
              <p>Para evitar que las esponjas internas se sequen mantener entre el 50% y 75 % de su capacidad.</p>
              <p>Es muy necesario limpiar bien los cabezales de impresi&oacute;n antes de poner el cartucho en la maquina y esperar por si gotea tinta despu&eacute;s de recargarlo. Si gotea dentro de la impresora, puede manchar donde descansa el cabezal, inundandolo de tinta, siendo necesaria su limpieza con mucho cuidado, al ser una parte muy delicada y vital de la impresora.</p>
              <p>Cada tres semanas sin imprimir dar orden de limpieza, para evitar que se sequen los cabezales</p>
              <p>Una de las mejores maneras de sellar el cartucho es utilizando plastelina, ya que cierra herm&eacute;ticamente y es muy f&aacute;cil de retirar.</p>
