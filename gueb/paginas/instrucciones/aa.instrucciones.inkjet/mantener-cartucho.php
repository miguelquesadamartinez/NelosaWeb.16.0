        <p>COMO MANTENER EL CARTUCHO EN PERFECTO ESTADO</p>
        <table width="97%"  border="0">
          <tr>
            <td><p align="left">Cada tres semanas sin imprimir, dar orden de limpieza, a los seis meses rellenar de tinta.</p>
              <p align="left"> Imprimir sin tinta puede quemar los cabezales ! !</p>
              <p align="left">El amarillo es el primero que se agota.</p>
              <p align="left"> El cabezal puede gotear hasta que expulse la tinta sobrante; Cartuchos con tinta liquida HP: asegurese de tapar bien el orificio de relleno, y presionar los laterales del cartucho mientras tapa, de forma que no tome aire, el orificio de relleno, si gotea, no est&aacute; bien cerrado.</p>
              <p align="left"> Si falla un color: Introducir 1ml del limpiador NELOSA o alcohol por el orificio de relleno (volvera a imprimir, pero con menos calidad) una vez reactivado el nivel de tinta recargar.</p>
              <p align="left"> Limpie el cabezal antes de ponerlo en la impresora, o cuando falle la impresi&oacute;n.<br>
                  Si la impresi&oacute;n sale MORADA, falta el amarillo, VERDE el magenta, ROSADA el Cyan.</p>
              <p align="left"> Al rellenar el cartucho, evitar introducir burbujas de aire, una vez lleno, extraer 1 ml de tinta a trav&eacute;s del cabezal. Cartuchos sin cabezal. Dar varias ordenes de limpieza espaciadas 5 minutos.</p>
              <p align="left"> Consulte en <a href="mailto:info@nelosa.net">info@nelosa.net</a> o www.nelosa.net : &ldquo;Gu&iacute;a de reparaci&oacute;n&rdquo; , &ldquo;<a href="../index.php">Instrucciones recarga</a>&rdquo; &ldquo;Recuperaci&oacute;n de cartuchos&rdquo;, &ldquo;Extractor universal de tinta&rdquo; , &ldquo;Reactivaci&oacute;n niveles tinta&rdquo; , &ldquo;Reseteador universal&rdquo; o en el C.D. de instrucciones.</p>
              <p align="left">Rellenar el cartucho cuando a&uacute;n le quede un 25% de su capacidad. En el caso de Epson con cartuchos separado, rellenar cuando est&eacute; a la mitad </p>
              <p>El cartucho debe estar fuera de impresora el menor tiempo posible, evitar&aacute; que el cabezal se seque<br>
                  <br>
                  Recargar la tinta muy lentamente y no introducir aire en el interior del cartucho. ANTES de iniciar la recarga lea atentamente las instrucciones generales y gu&iacute;a de recarga </p>
              <p>Antes de situar el cartucho en la impresora ,compruebe que no gotea y limpie el cabezal </p>
              <p>Compruebe si falla la impresi&oacute;n haciendo un Test de inyectores, conserve la hoja impresa, limpiar los cabezales y repetir el TEST y comprobar con el anterior si mejora. </p>
              <p>Limpieza de Cabezales. SIGA las instrucciones del manual de la impresora. Los ciclos de limpieza deben realizarse con un intervalo de 10 minutos.</p>
              <p>Si su cartucho no esta en las instrucciones, buscar otro modelo de impresora que tenga el mismo cartucho gui&aacute;ndose por el dibujo. Las instrucciones son las mismas.</p>
              <p>Si observa una alteraci&oacute;n el color, detenga la impresi&oacute;n, se agot&oacute; alguno de ellos.</p>
              <p>Evitar&aacute; quedarse sin tinta controlando peri&oacute;dicamente el peso del cartucho (10 ml. De tinta pesan 10 gramos), o extrayendo tinta a distintos niveles.</p></td>
